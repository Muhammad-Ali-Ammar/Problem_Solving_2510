#include <bits/stdc++.h>
using namespace std;

/*
  sto = String TO 
  stoi = string to I 
  stod = string to double 

*/
int main() 
{

    string s1 = "42";
    string s2 = "3.14";

    int num1 = stoi(s1);      
    double num2 = stod(s2);

    cout<<num1<<" "<<num2<<endl;  //42 3.14

  return 0;
}


